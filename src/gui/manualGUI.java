    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        try {
            Conector.connect();
            Conector.vaciarTabla(jTable1);
            Conector.modificarUsuario(mod_dni.getText(), listaCampos.getSelectedValue(), mod_text.getText());
            Conector.vaciarTabla(jTable1);
            Conector.llenarTabla(jTable1);
            Conector.close();
            
            mod_dni.setText("");
            mod_text.setText("");
            
        } catch (NullPointerException | NumberFormatException ex) {
            Conector.connect();
            Conector.vaciarTabla(jTable1);
            Conector.llenarTabla(jTable1);
            Conector.close();
        }
    }//GEN-LAST:event_btn_modificarActionPerformed

