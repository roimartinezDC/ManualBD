    private void btn_añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_añadirActionPerformed
        try {
            if (!text_dni.getText().equals("")) {
                Usuario usuario = new Usuario(text_dni.getText(), text_nombre.getText(), Integer.parseInt(text_cpost.getText()), text_fecha.getText(), text_correo.getText(), Integer.parseInt(text_tlf.getText()));
                Conector.connect();
                Conector.vaciarTabla(jTable1);
                Conector.insertarUsuario(usuario);
                Conector.vaciarTabla(jTable1);
                Conector.llenarTabla(jTable1);
                Conector.close();

                text_dni.setText(null);
                text_nombre.setText(null);
                text_cpost.setText(null);
                text_fecha.setText(null);
                text_correo.setText(null);
                text_tlf.setText(null);
            }
            
        } catch (NumberFormatException ex) {
            if (!text_dni.getText().equals("")) {
                Usuario usuario = new Usuario(text_dni.getText(), text_nombre.getText(), 0, text_fecha.getText(), text_correo.getText(), 0);
                Conector.connect();
                Conector.vaciarTabla(jTable1);
                Conector.insertarUsuario(usuario);
                Conector.vaciarTabla(jTable1);
                Conector.llenarTabla(jTable1);
                Conector.close();

                text_dni.setText(null);
                text_nombre.setText(null);
                text_cpost.setText(null);
                text_fecha.setText(null);
                text_correo.setText(null);
                text_tlf.setText(null);
            }
        }
        
    }//GEN-LAST:event_btn_añadirActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        Conector.connect();
        Conector.vaciarTabla(jTable1);
        Conector.eliminarUsuario(delete_dni.getText());
        Conector.vaciarTabla(jTable1);
        Conector.close();
        
        delete_dni.setText(null);
        Conector.connect();
        Conector.llenarTabla(jTable1);
        Conector.close();
    }//GEN-LAST:event_btn_eliminarActionPerformed

